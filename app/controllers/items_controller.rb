class ItemsController < ApplicationController
  before_filter :sign_in_required
  # GET /items
  # GET /items.json
  def index
    @items = Item.all

    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @items }
    end
  end

  def purchase
    @user = User.find(current_user.id)
    if !@user.purchase_item(params[:id])
      flash[:error] = "ERROR: Item not purchased, please check your balance on the profile page."
      redirect_to items_path
      return
    end
    @user.save!
    redirect_to user_path(current_user.id)
  end

  # GET /items/item_type
  def showHats
    @items = Item.where("item_type = ?", "Hat").order("price ASC")
  end

  def showTops
    @items = Item.where("item_type = ?", "Top").order("price ASC")
  end

  def showBottoms
    @items = Item.where("item_type = ?", "Bottom").order("price ASC")
  end

  def showShoes
    @items = Item.where("item_type = ?", "Shoes").order("price ASC")
  end

  # GET /items/1
  # GET /items/1.json
  def show
    @item = Item.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @item }
    end
  end

  # GET /items/new
  # GET /items/new.json
  def new
    @item = Item.new

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @item }
    end
  end

  # GET /items/1/edit
  def edit
    @item = Item.find(params[:id])
  end

  # POST /items
  # POST /items.json
  def create
    @item = Item.new(params[:item])

    respond_to do |format|
      if @item.save
        format.html { redirect_to @item, notice: 'Item was successfully created.' }
        format.json { render json: @item, status: :created, location: @item }
      else
        format.html { render action: "new" }
        format.json { render json: @item.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /items/1
  # PUT /items/1.json
  def update
    @item = Item.find(params[:id])

    respond_to do |format|
      if @item.update_attributes(params[:item])
        format.html { redirect_to @item, notice: 'Item was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @item.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /items/1
  # DELETE /items/1.json
  def destroy
    @item = Item.find(params[:id])
    @item.destroy

    respond_to do |format|
      format.html { redirect_to items_url }
      format.json { head :no_content }
    end
  end
end
